# print odd numbers
ev_num = list(range(1,11,2))
print(f"{ev_num}\n")


# square_numbers
squares = []

for val in range(1,11):
    squares.append(val*val)
print(f"{squares}\n")


# cube numbers
cubes = []

for val in range(1,11):
    cube = val**3
    cubes.append(cube)
print(cubes)