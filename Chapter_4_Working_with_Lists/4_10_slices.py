### 4-10. Slices:

foods = ["kinoa", "nahut", "fokacha", "oves", "zapekanka", "riba", "pizza", "spanak", "oriz"]

print(f"\nThe first three items in the list are: {foods[:3]}")

mid_foods = foods[3:6]

print(f"\nThree items from the middle: {mid_foods}")

print(f"\nLast three items: {foods[-3:]}")
