### 5-6. Stages of Life:

age = 70

if age < 2:
    message = "Person is a baby"
elif age < 4:
    message = "Person is a toddler"
elif age < 13:
    message = "Person is a kid"
elif age < 20:
    message = "Person is a teenager"
elif age < 65:
    message = "Person is a adult"
else:
    message = "Person is a elder"

print(message)
